package org.bflow.toolbox.hive.addons.utils;

import java.net.URL;

/**
 * Provides a data model for a prolog addition.
 * @author Arian Storch
 * @since 07/06/11
 */
public class PrologAddition {
	/**
	 * unique id of the prolog addition
	 */
	public String id;
	
	/**
	 * url to the prolog addition file 
	 */
	public URL url;
	
	/**
	 * name of the prolog addition
	 */
	public String name;
}
